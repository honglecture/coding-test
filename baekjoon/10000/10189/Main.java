import java.io.*;
public class Main {
    public static void main(String[] args) throws IOException {
        BufferedReader bf = new BufferedReader(new InputStreamReader(System.in));
        BufferedWriter bw = new BufferedWriter(new OutputStreamWriter(System.out));
        System.out.println("#  # #### #### #  #");
        System.out.println("#### #  # #  # # #");
        System.out.println("#### #  # #  # # #");
        System.out.println("#  # #### #### #  #");
        bw.flush();
        bw.close();
    }
}


